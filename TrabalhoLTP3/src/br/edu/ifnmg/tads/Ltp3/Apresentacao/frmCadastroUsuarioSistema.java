    protected void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(JOptionPane.showConfirmDialog(rootPane, "VocÃª tem certeza que deseja Salvar ?","",JOptionPane.OK_CANCEL_OPTION) == 0){
            if(validaCampos()){
                UsuarioSistema usuario = null;
                try{
                    usuario = new UsuarioSistema();
                    
                    usuario.setCpf(txtCpf.getText());
                    usuario.setNome(txtNome.getText());
                    usuario.setDataNascimento(dataFormatada(txtDataNascimento.getText()));
                    usuario.setRg(txtRg.getText());
                    usuario.setUsuario(txtUsuario.getText());
                    usuario.setSenha(txtSenha.getText());
                    usuario.setEnderecos(this.listaEnderecos);
                    usuario.setTelefones(this.listaTelefone);
                    usuario.setId(this.idPessoa);
                    usuario.setIdUsuario(this.idUsuario);
                            
                    
                    
                    }catch(ErroValidacaoException ex ){
                        JOptionPane.showMessageDialog(rootPane, ex.getMessage());
                        ex.printStackTrace();
                    }catch(Exception ex){
                        JOptionPane.showMessageDialog(rootPane, ex.getMessage());
                        ex.printStackTrace();
                    }
                    if(usuario != null){
                        UsuarioSistemaDAO dao = new UsuarioSistemaDAO();
                        try{
                            if(dao.Salvar(usuario)){
                                JOptionPane.showMessageDialog(rootPane, "UsuÃ¡rio Adicionado com Sucesso!");
                                limpaCampos();
                                this.listaEnderecos.clear();
                                this.listaTelefone.clear();
                                this.listaEmail.clear();
                                tblEnderecos.repaint();
                                tblTelefones.repaint();
                                tblEmails.repaint();
                                
                            }
                        }catch(ErroValidacaoException ex){
                            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
                        }catch(Exception ex){
                            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
                        }
                    }
                    
                
            }else{
                JOptionPane.showMessageDialog(rootPane,"Todos os Campos devem ser preenchidos");
            }
        
        
        
        }
        
        
    }//GEN-LAST:event_btnSalvarActionPerformed

   

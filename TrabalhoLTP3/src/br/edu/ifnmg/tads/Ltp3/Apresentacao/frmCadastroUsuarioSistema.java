    protected boolean validaCampos(){
        boolean valida = true;
        if( txtCpf.getText().isEmpty() || txtDataNascimento.getText().isEmpty() || 
             txtNome.getText().isEmpty() || txtUsuario.getText().isEmpty() 
            && !txtSenha.getText().equals(txtSenhaNovamente.getText())){
            
                valida = false;
            
        }else {
            valida = true;
        }
        
        return valida;
    
    }
    
